// JavaScript can 'display data in different ways

// 'Console.log' writes into the browser:
console.log(4) // will print 4
console.log('JavaScript') // will print JavaScript
console.log(true) // will print true

// 'innerHTML' writes into an HTML element:
/*
<script>
   document.getElementById("demo").innerHTML = 5 + 6;
</script>
 */

// 'document.write()' writes into the HTML output:
/*
<script>
    document.write(5 + 6);
</script>
* The document.write() method should only be used for testing.
 */

// 'windows.alert()' writes into an alert box:
/*
<script>
    window.alert(5 + 6);
</script>
 */