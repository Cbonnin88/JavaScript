/*
Assignment Operators assign values to JavaScript variables
*/

// The '=' operator assigns a value to a varible:
let x = 10;
console.log(x)

// The '+=' operator adds a value to a variable:
let a = 20;
a += 4;
console.log(a)

// The '-=' assignment operator subtracts a value from a variable:
let b = 100;
b -= 20;
console.log(b);

// The '*=' assignment operator multiplies a value from a variable:
let c = 12;
c *= 200;
console.log(c);

// The '/=' assignment operator divides a variable:
let d = 32;
d /= 2;
console.log(d)

// The '%=' assignment operator assigns a remainder to a variable:
let e = 9;
e %= 4;
console.log(e);
